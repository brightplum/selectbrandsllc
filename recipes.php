<dt class="recipe"><a class="null" href="#" alt="Homemade Barbecue Marinade">Homemade Barbecue Marinade</a></dt>
<dd class="recipe">
<div class="bgwhite">
<p class="bold">Homemade Barbecue Marinade</p>
<p>
	<ul>
		<li>1 1/2 cups vegetable oil</li>
		<li>1 tbsp black pepper</li>
		<li>3/4 cup soy sauce</li>
		<li>1/2 cup wine vinegar</li>
		<li>1/4 cup Worcestershire sauce</li>
		<li>1 1/2 tsp dried parsley flakes</li>
		<li>2 tbsp dry mustard</li>
		<li>2 cloves crushed garlic</li>
		<li>2 1/4 tsp salt</li>
		<li>1/3 cup fresh lemon juice</li>
		<li>10 lbs desired meat (spare ribs, beef ribs or steaks)</li>
		<li>Mumbo<sup>&reg;</sup> Bar-B-Q Sauce (Original, Hickory Smoke or Tangy flavors)</li>
		<li>Hickory Barbecuing Chips</li>
	</ul>
</p>
<p>Combine all ingredients, except Mumbo<sup>&reg;</sup> Sauce and meat, and mix well. 
Place meat in a large bowl and pour over marinade. Turn meat to coat all sides. 
Cover bowl and refrigerate for at least 2 hours. Pile charcoal in a mound in the center of the grill; 
sprinkle hickory chips around coals and apply lighter fluid. 
Let stand for 2-3 minutes and then light charcoal. 
Let coals burn until they turn ash gray then spread mound evenly across grill. 
Place marinated meat on grill and turn approx. every 4-5 min. until done. 
Baste meat with Mumbo<sup>&reg;</sup> Sauce 5 minutes before removing from grill. 
Warm the remainder of Mumbo<sup>&reg;</sup> Sauce and serve on the side for dipping.</p>
