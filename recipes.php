<dd class="recipe">
<div class="bgwhite">
<p class="bold">Cajun Barbecue Shrimp</p>

<p>
	<ul>
		<li>1 pound Jumbo Raw Shrimp peeled & deveined with tail left on.</li>
		<li>1 tbsp Lemon Juice</li>
		<li>1 tbsp Worcestershire Sauce</li>
		<li>1 tbsp Coarsely Ground Pepper</li>
		<li>1/2 tsp salt</li>
		<li>Butter</li>
