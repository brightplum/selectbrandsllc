</p>
<p>Put shrimp in single layer 9x13 inch baking dish. Dot with butter. 
Combine remaining ingredients, and pour over shrimp. 
Bake in preheated 350 degree oven for 15 minutes or until sauce is thoroughly hot. 
Serve immediately with Spiced Rice.
Makes 4 servings</p>



<p class="bold">Spiced Rice</p>
<p>
	<ul>
		<li>1/2 cup sliced Green Onions</li>
		<li>1/2 cup minced carrots</li>
		<li>1/2 cup minced red pepper</li>
		<li>1 jalapeno or serrano pepper minced</li>
		<li>1 tbsp vegetable oil</li>
		<li>2 cups cooked rice (cooked in chicken broth)</li>
		<li>2 tbsp snipped fresh cilantro</li>
		<li>1 tbsp lime juice</li>
		<li>1 tsp soy sauce</li>
		<li>Hot Pepper Sauce to taste</li>
	</ul>
</p>

<p>Saute onions, carrots and peppers in vegetable oil in a large skillet. 
Over low flame add lime juice, soy sauce and hot pepper sauce to taste. Stir in rice until heated. 
Remove from flame and garnish with fresh cilantro. Serve with Barbecue Shrimp.</p>
